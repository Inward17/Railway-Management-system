    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String tname = jTextField1.getText();
        
        String tsrc = jTextField5.getText();
        
        String tdest = jTextField4.getText();
        
        try {
            DatabaseConnection dc = new DatabaseConnection();
            dc.connect();
            dc.insertTrain(tname, tsrc, tdest);
            dc.disconnect();
            
            JOptionPane.showMessageDialog(this, "Train added", "Success", JOptionPane.INFORMATION_MESSAGE);
        } catch(Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(this, "Error while adding train", "Error", JOptionPane.ERROR_MESSAGE);
        }      
        
    }//GEN-LAST:event_jButton1ActionPerformed

